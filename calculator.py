# Claculator file, contains static methods for all calculator functionality

# Division of two numbers, note: throws exception if y = 0
def division(x: float, y: float) -> float:
    return x/y

def addition(num1:float, num2:float):
    return(num1 + num2)

# Subtraction of two numbers, takes in 2 floats and returns a float
def subtraction(num1: float, num2: float) -> float:
    return (num1 - num2)

# Multiplies two numbers and returns the product
def multiply(num1:float, num2:float):
    return num1*num2